num1=10
num2=20

# Addition
add=num1+num2
print(f"Addition of num1 + num2 ={add}")

#substraction
sub=num1-num2
print(f"Substraction of num1-num2= {sub}")

# multiplication
mul=num1*num2
print(f"Multiplication of num1 * num2 = {mul}")

# division
div=num1/num2
print(f"Division of num1 / num2= {div}")

# floor division
floor=num1//num2
print(f"Floor division of num1//num2= {floor}")

# exponent
exponent=num1**num2
print(f"Exponent of num1 ** num2={exponent}")

# modulus
modulo=num1%num2
print(f"Modulus of num1 % num2 = {modulo}")
